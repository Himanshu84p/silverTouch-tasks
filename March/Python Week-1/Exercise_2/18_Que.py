# Use all the validation methods used with string

string_1 = "Python777"

print("isalnum()",string_1.isalnum())
print("isalpha()",string_1.isalpha())
print("isascii()",string_1.isascii())
print("isdecimal()",string_1.isdecimal())
print("isdigit()",string_1.isdigit())
print("isidentifier()",string_1.isidentifier())
print("islower()",string_1.islower())
print("isnumeric()",string_1.isnumeric())
print("isprintable()",string_1.isprintable())
print("isspace()",string_1.isspace())
print("istitle()",string_1.istitle())
print("isupper()",string_1.isupper())
print("startswith()",string_1.startswith('P'))