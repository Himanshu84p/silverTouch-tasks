varFloat = 5.72
print("value of float variable is :", varFloat)
print("type of it : ",type(varFloat))  

varInt = int(varFloat)
print("\nValue of Int variable is: ", varInt)
print("type of it : ",type(varInt)) 

varBoolean = bool(varFloat)
print("\nValue of boolean variable is: ", varBoolean)
print("type of it : ",type(varBoolean)) 

varString = str(varFloat)
print("\nValue of string variable is: ", varString)
print("type of it : ",type(varString)) 