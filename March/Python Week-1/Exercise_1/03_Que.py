varBoolean = True
print("value of boolean variable is :", varBoolean)
print("type of it : ",type(varBoolean))

varInt = int(varBoolean)
print("\nvalue of integer variable is:", varInt)
print("type of it : ",type(varInt))

varFloat = float(varBoolean)
print("\nvalue of float variable is:", varFloat)
print("type of it : ",type(varFloat))

varString = str(varBoolean)
print("\nvalue of string variable is:", varString)
print("type of it : ",type(varString))
