varString = "25"
print("value of string variable is :", varString)
print("type of it : ",type(varString))

varInt = int(varString)
print("value of Integer variable is :", varInt)
print("type of it : ",type(varInt))

varFloat = float(varString)
print("value of float variable is :", varFloat)
print("type of it : ",type(varFloat))

varBoolean = bool(varString)
print("value of Boolean variable is :", varBoolean)
print("type of it : ",type(varBoolean))
