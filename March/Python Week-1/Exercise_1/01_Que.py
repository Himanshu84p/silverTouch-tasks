varInt = int(5)
print("value of integer variable is :", varInt)
print("type of it : ",type(varInt))

convertFloat = float(varInt)
print("\nvalue of float is :",convertFloat)
print("type of it : ",type(convertFloat))

convertBoolean = bool(varInt)
print("\nvalue of boolean is :",convertBoolean)
print("type of it : ",type(convertBoolean))

convertString = str(varInt)
print("\nvalue of string is :",convertString)
print("type of it : ",type(convertString))

