#Perform operations with all the Bitwise Operators

# AND
a = 10    # 1010 in binary
b = 5     # 0101 in binary
result_and = a & b
print("Bitwise AND:", result_and)  

# OR
result_or = a | b
print("Bitwise OR:", result_or)    

# XOR
result_xor = a ^ b
print("Bitwise XOR:", result_xor)  

# NOT
result_not_a = ~a
print("Bitwise NOT a:", result_not_a)  

# Shift
result_left_shift = a << 2  
print("Left Shift of a:", result_left_shift)  
# Shift
result_right_shift = a >> 1 
print("Right Shift of a:", result_right_shift)  
